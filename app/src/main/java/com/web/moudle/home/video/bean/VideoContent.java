package com.web.moudle.home.video.bean;

public class VideoContent {
    private String content_id;
    private String duration;
    private String del_status;
    private String pic_large;
    private String title;
    private String pic_middle;
    private String pic_small;
    private String source;
    private String artist_name;
    private String url;

    public String getContent_id() {
        return content_id;
    }

    public void setContent_id(String content_id) {
        this.content_id = content_id;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getDel_status() {
        return del_status;
    }

    public void setDel_status(String del_status) {
        this.del_status = del_status;
    }

    public String getPic_large() {
        return pic_large;
    }

    public void setPic_large(String pic_large) {
        this.pic_large = pic_large;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPic_middle() {
        return pic_middle;
    }

    public void setPic_middle(String pic_middle) {
        this.pic_middle = pic_middle;
    }

    public String getPic_small() {
        return pic_small;
    }

    public void setPic_small(String pic_small) {
        this.pic_small = pic_small;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getArtist_name() {
        return artist_name;
    }

    public void setArtist_name(String artist_name) {
        this.artist_name = artist_name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
